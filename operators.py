#Arithmetic Operation - Simple calculation

X = 34
Y = 20

print(X + Y)
print(X - Y)
print(X * Y)
print(X / Y)
print(X % Y) #Modulus - Returns the remainder - Compare values

#Comparison Operators
print(X < Y)
print(X > Y)
print(X <= Y)
print(X >= Y)
print(X == Y) #Equal to
print(X != Y) #Not equal to

#Assignment operators (=)
number = 10
print(number)

#operator Precedence
number %= 2
print(number)


# logical operators- and, or, not
num1 = 60
num2 = 12
print(num1 == num2 and num1 > num2)
print(num1 == num2 or num1 > num2)
print(not(num1 == num2 or num1 > num2))

